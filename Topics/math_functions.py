import math
n = float(input("enter a number : "))


square = math.sqrt(n)
print("square : ",square)

floor = math.floor(n)
print("floor : ",floor)

ceil = math.ceil(n)
print("Ceil : ",ceil)

power = math.pow(n,3)
print(f"{n} to the power 3 : ",power)

pi = math.pi
print("PI : ",pi)

exponantial = math.e
print("exponantial : ",exponantial)
